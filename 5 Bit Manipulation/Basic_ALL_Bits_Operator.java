public class Basic_ALL_Bits_Operator {
   // 1) Binary AND &
    public static void main(String[] args) {
        System.out.println((5 & 6));
    }

    // 2) Binary OR | 
    public static void main(String[] args) {
        System.out.println(( 5 | 6 ));
    }

    // 3) Binary XOR ^

    public static void main(String[] args) {
        System.out.println(( 5 ^ 6));
    }

  // 4)  Binary not 

  public static void main(String[] args) {
    System.out.println((~5)); 
  }

  // 5) Binary Left Shiff <<

  public static void main(String[] args) {
    System.out.println((5<<2));
  }

  // 6) Binary Right Shiff >.

  public static void main(String[] args) {
    System.out.println((5>>2));
  }
  
  // 7) 

    
}